-- Public profile table used by the Flask app.
-- auth.users stores the real auth account, while app_users lets the app
-- check duplicate emails and keep a simple user list for the project.
-- After running this file in Supabase SQL Editor, PostgREST is notified to
-- refresh its schema cache so new columns are visible to the REST API.
create table if not exists public.app_users (
    id uuid primary key,
    email text unique not null,
    full_name text,
    role text not null default 'user',
    phone_number text,
    profile_image text,
    created_at timestamptz not null default now()
);

create table if not exists public.menu_items (
    id bigint generated by default as identity primary key,
    name text not null,
    description text not null,
    category text not null,
    price numeric(10,2) not null,
    image text not null,
    is_available boolean not null default true,
    sizes jsonb -- For beverages: {"small": 20.0, "medium": 25.0, "large": 30.0}
);

create table if not exists public.orders (
    id bigint generated by default as identity primary key,
    customer_name text not null,
    order_number integer,
    table_number text,
    items jsonb not null,
    total_amount numeric(10,2) not null,
    payment_method text not null,
    payment_bank text,
    payment_reference text,
    order_type text not null default 'Delivery',
    delivery_address text,
    delivery_notes text,
    status text not null default 'Pending',
    created_at timestamptz not null default now()
);

alter table public.orders add column if not exists payment_method text;
alter table public.orders add column if not exists payment_bank text;
alter table public.orders add column if not exists payment_reference text;
alter table public.orders add column if not exists order_type text not null default 'Delivery';
alter table public.orders add column if not exists order_number integer;
alter table public.orders add column if not exists delivery_address text;
alter table public.orders add column if not exists delivery_notes text;
alter table public.app_users add column if not exists full_name text;
alter table public.app_users add column if not exists role text not null default 'user';
alter table public.app_users add column if not exists phone_number text;
alter table public.app_users add column if not exists profile_image text;
alter table public.menu_items add column if not exists is_available boolean not null default true;

-- Keep existing rows valid after adding these columns to an older database.
update public.orders
set payment_method = 'Cash'
where payment_method is null;

update public.orders
set order_type = 'Delivery'
where order_type is null or btrim(order_type) = '';

update public.orders
set order_number = id::integer
where order_number is null;

create sequence if not exists public.orders_order_number_seq;

select setval(
    'public.orders_order_number_seq',
    greatest(coalesce((select max(order_number) from public.orders), 0), 0) + 1,
    false
);

alter table public.orders
alter column order_number set default nextval('public.orders_order_number_seq');

create unique index if not exists orders_order_number_unique
on public.orders(order_number)
where order_number is not null;

update public.app_users
set full_name = initcap(replace(split_part(email, '@', 1), '.', ' '))
where full_name is null or btrim(full_name) = '';

update public.app_users
set role = 'user'
where role is null or btrim(role) = '';

update public.menu_items
set is_available = true
where is_available is null;

