do $$
begin
    if not exists (
        select 1
        from information_schema.table_constraints
        where table_schema = 'public'
          and table_name = 'orders'
          and constraint_name = 'orders_rider_id_fkey'
    ) then
        alter table public.orders
        add constraint orders_rider_id_fkey
        foreign key (rider_id) references public.riders(id)
        on delete set null;
    end if;
end $$;

-- Keep existing rows valid after adding these columns to an older database.
update public.orders
set payment_method = 'Cash'
where payment_method is null;

update public.orders
set payment_status = 'Pending'
where payment_status is null or btrim(payment_status) = '';

update public.orders
set order_type = 'Delivery'
where order_type is null or btrim(order_type) = '';

update public.orders
set delivery_option = case
    when order_type ilike 'pickup' then 'Pickup'
    else 'Delivery'
end
where delivery_option is null or btrim(delivery_option) = '';

update public.orders
set delivery_status = 'Waiting'
where delivery_status is null or btrim(delivery_status) = '';

update public.orders
set order_number = id::integer
where order_number is null;

create sequence if not exists public.orders_order_number_seq;

select setval(
    'public.orders_order_number_seq',
    greatest(coalesce((select max(order_number) from public.orders), 0), 0) + 1,
    false
);

alter table public.orders
alter column order_number set default nextval('public.orders_order_number_seq');

create unique index if not exists orders_order_number_unique
on public.orders(order_number)
where order_number is not null;

update public.app_users
set full_name = initcap(replace(split_part(email, '@', 1), '.', ' '))
where full_name is null or btrim(full_name) = '';

update public.app_users
set role = 'user'
where role is null or btrim(role) = '';

update public.menu_items
set is_available = true
where is_available is null;

update public.menu_items
set stock_quantity = 0
where stock_quantity is null or stock_quantity < 0;

update public.menu_items
set low_stock_threshold = 5
where low_stock_threshold is null or low_stock_threshold <= 0;

update public.menu_items
set is_available = false
where stock_quantity <= 0;

update public.riders
set status = 'Available'
where status is null or btrim(status) = '';

update public.employees
set attendance_status = 'Off Duty'
where attendance_status is null or btrim(attendance_status) = '';

update public.employees
set employment_status = 'Active'
where employment_status is null or btrim(employment_status) = '';

