-- Public profile table used by the Flask app.
-- auth.users stores the real auth account, while app_users lets the app
-- check duplicate emails and keep a simple user list for the project.
-- After running this file in Supabase SQL Editor, PostgREST is notified to
-- refresh its schema cache so new columns are visible to the REST API.
create table if not exists public.app_users (
    id uuid primary key,
    email text unique not null,
    full_name text,
    role text not null default 'user',
    phone_number text,
    delivery_address text,
    profile_image text,
    created_at timestamptz not null default now()
);

create table if not exists public.menu_items (
    id bigint generated by default as identity primary key,
    name text not null,
    description text not null,
    category text not null,
    price numeric(10,2) not null,
    image text not null,
    is_available boolean not null default true,
    stock_quantity integer not null default 0,
    low_stock_threshold integer not null default 5,
    sizes jsonb -- For beverages: {"small": 20.0, "medium": 25.0, "large": 30.0}
);

create table if not exists public.orders (
    id bigint generated by default as identity primary key,
    customer_name text not null,
    customer_email text,
    order_number integer,
    table_number text,
    items jsonb not null,
    total_amount numeric(10,2) not null,
    payment_method text not null,
    payment_status text not null default 'Pending',
    payment_bank text,
    payment_reference text,
    payment_proof text,
    order_type text not null default 'Delivery',
    delivery_option text not null default 'Delivery',
    delivery_address text,
    preferred_time text,
    delivery_notes text,
    rider_id uuid,
    delivery_status text not null default 'Waiting',
    status text not null default 'Pending',
    inventory_deducted boolean not null default false,
    created_at timestamptz not null default now()
);

create table if not exists public.riders (
    id uuid primary key default gen_random_uuid(),
    name text not null,
    phone text,
    status text not null default 'Available'
);

create table if not exists public.employees (
    id uuid primary key default gen_random_uuid(),
    name text not null,
    position text not null,
    contact_number text,
    shift_schedule text,
    attendance_status text not null default 'Off Duty',
    employment_status text not null default 'Active',
    notes text,
    task_assignment text,
    time_in text,
    time_out text,
    created_at timestamptz not null default now()
);

